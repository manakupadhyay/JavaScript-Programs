console.log(os.homedir())   // home directory

console.log(os.hostname())  // host name

console.log(os.networkInterfaces()) // newtwork interfaces available

console.log(os.platform())  // platform

console.log(os.type())  // type of os

console.log(os.userInfo())  // user information





